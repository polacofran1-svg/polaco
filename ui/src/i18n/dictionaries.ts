// Helper type to ensure type safety when accessing nested keys
export type TranslationKey =
  | `sidebar.${keyof typeof dictionaries.en.sidebar}`
  | `common.${keyof typeof dictionaries.en.common}`
  | `templates.${keyof typeof dictionaries.en.templates}`
  | `modals.newIssue.${keyof typeof dictionaries.en.modals.newIssue}`
  | `modals.newAgent.${keyof typeof dictionaries.en.modals.newAgent}`
  | `modals.newProject.${keyof typeof dictionaries.en.modals.newProject}`
  | `modals.newGoal.${keyof typeof dictionaries.en.modals.newGoal}`;

export function getTranslation(dict: typeof dictionaries.en, keyString: TranslationKey): string {
  const parts = keyString.split('.');
  let current: any = dict;
  for (const part of parts) {
    if (current && typeof current === 'object' && part in current) {
      current = current[part];
    } else {
      return keyString; // Fallback to key if not found
    }
  }
  return typeof current === 'string' ? current : keyString;
}
